#digite um algoritimo que recebe 3 salários e imprime a media salarial:

salario1 = float(input("digite o primeiro salario: "));
salario2 = float(input("digite o segundo salario: "));
salario3 = float(input("digite o terceiro salario: "));

media_salarial = ((salario1+salario2+salario3)/3)
print("a média salarial é:",media_salarial)

"""Q5. Escreva um algoritmo que leia a nota de 3 estudantes e retorne a média das notas.
"""


nota1 = float(input("Qual a nota do primeiro estudante? "));
nota2 = float(input("Qual a nota do segundo estudante? "));
nota3 = float(input("Qual a nota do terceiro estudante? "));

media =((nota1+nota2+nota3)/3);

print("a media de notas é:", media);
